
void main(List<String> args) {
  //operators is any symbol like + - < 
  //  Arithmetic operators
  print(1+2); // 3
  print(5-3); // 2

  print(5*2); //10 

  print(10/2); // 5.0
  print(5/2); // 2.5

  print(10~/2);//5
