  // / return double


  print(10%2); // 0
  print(4%3); // 1
  // % Get the remainder of an integer division , it is name in programming is 'modulo' 

  // 
  int x=1;
  x+=1 ; // x= x+1 ; //2
  // same for all Arithmetic operators
  x *=4; // x = x * 4;

  int y = 1;
  y++ ; // y=y+1 // 2
  y--;
}